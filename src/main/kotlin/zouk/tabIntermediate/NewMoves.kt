package zouk.tabIntermediate

import common.youtube
import kotlinx.html.*
import tabs.SectionedTabDsl

fun ZoukIntermediateSections.newMoves(dsl: SectionedTabDsl) {
    with(dsl) {
        section("New Moves") {
            subSection("Elastico") {
                html {
                    p {
                        +"Start in a basic, holding hands in a further away distance. "
                        +"It can also done from a lateral step, then you need to step back once you are on the right side. "; br
                        +"When positioned at the back, stay on the spot to increase the distance further. "
                        +"Watch out to clearly indicate the difference between going into lateral (rotational pull) and staying straight for an Elastico (although positioned on the side). "; br
                        +"Let go of the right hand and step out of the line. "
                        +"Let go of the left hand and use the right to pull the follower back on the hip. "
                        +"Slide in with the left over the shoulder and arm to go back to basic. "
                    }
                    youtube("DaQ4HhR7Ekc?si=0e2rCdM-jJiUSbG8", "Elastico move from basic")
                    subSubSection("Elastico with Raul") {
                        html {
                            p {
                                +"You can add a Raul (Portuguese meaning 'to vomit') which is due to the nature of the wave travelling the body upwards. "
                                +"Step the left leg forward, raise your body up along with the arm, which will make her body go up as well. "
                            }
                            youtube("Wwm1PGBE8sk?si=f4TBjO6_mg0sndIe", "Elastico with Raul")
                        }
                    }
                }
            }
            subSection("Hammer Lock") {
                html {
                    p {
                        +"While being in lateral, instead of doing a simple turn, we now lift the right arm (not the left). "
                        +"Using ";b { +"crab steps" };+" to walk to the right and left side parallel. "
                        +"On the first step to the left, we are now positioned in a lunge. "
                        +"The lifted (right) arm draws kind of a halo around the (fore-)head of the follower.";br
                        +"Watch out that the left hand stays connected (";i { +"locked" };+") throughout with a soft palm connection using a moderate amount of pressure; oomph-quality. "
                        +"Extend the arm in the middle of the turn, as otherwise the follower will have issues in their shoulders. "
                        +"Also bend it again to have it on the waist, instead of the butt. Don't bend the elbow, to prevent unintentional boob grazing. "; br
                        +"To go back to basic, lift the left hand (switching from right to left), and do a simple turn. "
                        +"Don't forget to slide over the shoulder blade as usual to indicate going back to a basic. "
                    }
                    youtube("FjlsyVHGza4?si=F5ZhNJzuEfxmMy0j", "Hammer Lock Lunge Turn with explanations")
                    subSubSection("Lateral Variation") {
                        html {
                            p {
                                +"A variation would be to do it more in a ";b { +"circular way" };+". "
                                +"The leader going around her, clockwise, while the left hand stays connected and also very low (follower's shoulder!). "
                                +"The follower simply stays in lateral all the time; the leader also does the follower's lateral steps. "
                                +"To get out of it, raise the arm up, and then need to do some quick steps to get back to regular lateral again. "
                            }
                            youtube("uT_o_z2wUtc?si=vwUD-iLNIL_TZEug", "Hammer Lock Lateral Variation")
                        }
                    }
                    subSubSection("Soltinho Variation") {
                        html {
                            p {
                                +"Similarly, instead of Lateral, you can use the Soltinho to enter and exit the Hammerlock. "
                                +"During Soltinho, put the hand down to enter it, and switch to Lateral steps. "; br
                                +"You can also ";b { +"walk in front" };+" of your follower and start to combine all those variations (with one or two hands holding). "
                            }
                            youtube("xopz7xGtx04?si=cdjBMxFn9xe0H8fi", "Hammer Lock Soltinho Variation")
                        }
                    }
                }
            }
            subSection("Airplanes") {
                html {
                    p {
                        +"Instead of leading a ";b { +"simple turn" };+" from lateral to basic, you can also rotate the follower into an airplane. "
                        +"The name stems from the extended arms rotating like the propeller of an airplane (or like the Dutch windmills). "
                        +"Left hand slides ";i { +"under" };+" the follower's elbow, the right hand ";i { +"above" };+" on the elbow pit. "
                        +"Shift your weight slightly to the left, going into a lunge position. "
                        +"Give a slight push to indicate the movement, maintaining frame connection."
                        +"The follower turns on the spot as usual; avoid travelling while turning, as the positioning will be misplaced otherwise. "
                    }
                    p {
                        +"Another way of implementing the airplane is by the leader ";b { +"synchronizing" };+" into the follower's rhythm. "
                        +"In partner dance, this is generally also called to ";b { +"shadow" };+" the partner. "; br
                        +"Start by changing your hand connection, only handing with your right hand the follower's left hand. "
                        +"Move in front of her facing her with your back. "
                        +"When doing the lunge, add a delay to sync into her rhythm, and do the turn. "
                        +"You need to be a bit faster then the follower, slide your hand on her back, and add another delay to sync back to your own rhythm. "; br
                        +"The follower can opt to do those airplane/windmills with an added head movement for more styling. "
                    }
                    youtube("H5V6KIGmaCs?si=MQ5m1-lwSVfVkNtD", "Airplane Variations")
                }
            }
            subSection("Shoulder Embrace") {
                html {
                    p {
                        +"Start from lateral and change hands (on beat 5), taking the follower's right hand with your own right hand. "
                        +"She will then turn counter-clockwise three quarters in such a way that the line will be moved 90 degrees. "
                        +"Reach with your left the follower's left hand, collecting her, and have both on/around the shoulders. "; br
                        +"From here, you have all the possibilities like in the regular embrace: body-waves, bounces, walking (on-beat), chicote w/o cambre. "
                    }
                    p {
                        +"To get back to basic, it's almost the same as with the regular embrace. "
                        +"Stay on the spot while the follower walks to the right. "
                        +"Let go of the right hand and reach for the scapula, while you put her left  hand over your own head on the shoulder. "
                        +"This can be done very slowly, take your time, until the follower rests on your arm again. "
                    }
                    youtube("zrkxfnTSkbM?si=kDY0PJzIHIkEYPKF", "Shoulder Embrace with walking / bounce / body-wave")
                }
            }
            subSection("Bounce-Bodywave") {
                html {
                    p {
                        +"There needs to be a proper, firm connection through the hands and arms so communication can be flow when indicating this technique. "
                        +"When leaning backwards, the common \"LEGO\"-hands give enough surface to hold on to each other; elbow straight, arms extended and facing downwards. "
                        +"When leaning forward, the leader provides a surface, palms facing upwards, onto which the follower is pushing down (alternatively, a typical LEGO-hand push can be done as well). "
                        +"This requires a slight outward rotation of the wrist, and keep the wrists always straight to prevent over-exhaustion of the joints. "
                    }
                    p {
                        +"Play around with a rhythmic pattern (on-beat, or double-time) of ";b{+"bouncing"};+" front and back together, maintaining a stable frame and an elastic quality to make this motion as smooth as possible. "
                        +"The elastic frame acts as a form of \"dampener\", by keeping the elbows and shoulders soft, making it more round than edgy/sharp/potentially uncomfortable. "
                        +"The body-wave is quite similar in that the leader is indicating the move by simply doing it himself, with a slight indication with drawing a circular shape with his arms. "
                    }
                    p {
                        +"Alternatively, you can also try to do it in a ";b { +"open hand position" };+", instead of a closed one. "
                        +"The leader is flattening the hands, follower doing the same, so the palm contact can be maintained while the arms are rotating; the leader's fingers now pointing outwards. "
                    }
                    p {
                        +"While doing the lateral steps, when the follower is on the left and turning facing the right, step on her line and give a \"blocking\" signal to make her stand. "
                        +"To get back to lateral, simple step out of the line and let her repeat her lateral where she left off. "
                    }
                    youtube("Rnm6g4pOuLA?si=hLi0XpIfhQccp4Pa", "Bounce and Body-wave from Lateral")
                }
                subSubSection("Bodywaves in Frontal Stance") {
                    html {
                        p {
                            +"Leading from the shoulders, also called ";b{+"snake"};+", the arms go down- and inwards (towards yourself). "
                            +"Leaning forward lowers the arms, shifting we weight back, up, and forward again."
                            +"As usual, when leading also doing the movement yourself will result in giving the right and a clear signal (also visually). "
                            +"The shape of the hand resembles a circle, which can be a regular circle or more of a (horizontal) oval to make it bigger. "
                        }
                        p {
                            +"Leading from the hips, also called ";b{+"seaweed"};+" (or waterplant), is a circle (horizontal oval) in the other direction. "
                            +"By leaning backwards, the motion goes first up- and then outwards (away). "
                            +"The oval shape will lead to bigger shift of the weight. "
                        }
                        p {
                            +"Chicote at the end is basically the same as seaweed, but then the oval shape is oriented vertically (instead horizontally). "
                            +"Exactly the same as when we are in embrace, going more down-up, instead forward-backward. "; br
                            +"Change between the two circles only when the weight is back, never when it is on the front leg. "
                        }
                        // ... graphics of circles
                    }
                }
            }
            subSection("Chicote") {
                html {
                    youtube("H1wRPgekvIw?si=rhHOw9jYw1n_eGLt", "Lateral to Bodywave to Chicote")
                    p {
                        +"From lateral, step in front of the follower (into her line) and start with a bodywave (see above) and rotate the hand position externally. "
                        +"Then go straight down, and slowly roll to reverse the bodywave. "
                        +"Finally go down again, arms wide to the side and push the follower upright; going back to lateral."; br
                        +"When doing the bounce here, the hands stay at their exact same position in three-dimensional space, but when doing the bodywave, the hands draw the shape of a ";b { +"hockey stick" };+": "
                        +"A long, straight line downwards, with a short loop towards the leader."
                    }
                    p {
                        +"For the follower, it's like a whip (of the hair), but accentuated like a Raul. "
                        +"Don't go too far, don't over-extend it to the back, and don't do it too quickly to whiplash backwards (injury!). "
                        +"The leader maintains his head upwards and does not do the head move at all. "
                    }
                }
            }
            subSection("Cambre") {
                html {
                    p {
                        +"Also called \"dip\" (it's in the category of Chicote and Raul), is an illusion of a backwards bend; "
                        +"especially the lower back stays straight and doesn't bend. "
                        +"Anytime the follower is in a forward/back bodywave, a Chicote can be added, e.g. while in an Embrace. "; br
                        +"When rolling up from the Chicote, the leader's left arm goes up, and tilt your body slightly (!) to the right (not collapsing). "
                        +"Doing so will prevent the follower's back from over-extend and possibly injuring herself."
                    }
                    youtube("d6JNEBwPZec?si=mPruwyOqKUyTxOzf", "Bodywave, reverse, Chicote and Cambre")
                }
            }
        }
    }
}
