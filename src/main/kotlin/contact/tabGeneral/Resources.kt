In case you are looking to practice (classes, workshops, festivals) anywhere in the world, check out the \url{https://ciglobalcalendar.net}, and if you need something more local (in the Netherlands) lookup the community calendar \url{https://amsterdamjam.nl} or go directly to my teacher's website: \url{https://tomgoldhand.com}.

\section{Books}\label{sec:books}

There are about less than a dozen (good) books written specifically about CI\@:

\begin{itemize}
    \setlength\itemsep{0em}
    \item \res{https://www.amazon.com/Sharing-Dance-Improvisation-Directions-Anthropological/dp/0299124444}{Sharing the Dance: Contact Improvisation and American Culture} by Cynthia J. Novack (A good book about the history of the form, origins and stories)
    \item \res{https://www.amazon.com/Caught-Falling-Confluence-Contact-Improvisation/dp/0937645095}{Caught Falling: The Confluence of Contact Improvisation} by Nancy Stark Smith
    \item \res{https://www.amazon.com/Contact-Improvisation-Introduction-Vitalizing-Dance/dp/0786426470}{Contact Improvisation: An Introduction to a Vitalizing Dance Form} by Cheryl Pallant
    \item \res{https://www.amazon.com/Contact-Improvisation-Dancing-Interaction-Introduction/dp/1841261386}{Contact Improvisation: Moving, Dancing, Interaction} by Thomas Kaltenbrunner
    \item \res{https://www.amazon.com/Improvisation-Body-Mind-Centering-Teaching-Learning/dp/0976044900}{Contact Improvisation and Body-Mind Centering: A Manual for Teaching and Learning Movement} by Annie Brook
    \item \res{https://www.amazon.com/Taken-Surprise-Dance-Improvisation-Reader/dp/0819566489}{Taken by Surprise: A Dance Improvisation Reader} by Ann Cooper Albright and David Gere
    \item \res{https://www.amazon.com/Dancing-Deeper-Still-Practice-Improvisation/dp/1775243044}{Dancing Deeper Still: The Practice of Contact Improvisation} by Martin Keogh
    \\ \textbf{Review:} It offers some rather diverse insight into the world of CI, from very, very poetic and imaginary, almost like reading a very abstract poet, to very pragmatic like enumerating criteria of a seasoned CI practitioner and many other useful practicalities, enriched by Martin's personal experience as an international teacher for multiple decades.
    \item \res{https://contredanse.org/en/product/gravity/}{Gravity} by Steve Paxton
\end{itemize}

\section{Videos}\label{sec:videos}

It's good practice to watch ``the classics'' (everything between 1972 and 1980) once in a while, to check your understanding of the roots of CI or simply to get inspired to continue your practice.

\begin{itemize}
    \setlength\itemsep{0em}
    \item \res{https://www.youtube.com/watch?v=9FeSDsmIeHA}{The invention of CI} - The very early beginnings, 1972
    \item \res{https://www.youtube.com/watch?v=5gEfVJBhwrQ}{Magnesium} - The actual piece from that research is called magnesium, 1972
    \item \res{https://www.youtube.com/watch?v=xKlO-2e3gHo}{Chute} - A few years later, 1979
    \item \res{https://www.youtube.com/watch?v=k768K\_OTePM}{Fall After Newton} - Many things have changed here, 1987; the original length of this video is actually 22 minutes
    \item \res{https://www.youtube.com/@contactquarterly}{Contact Quarterly} - A YouTube channel containing some videos of the early beginnings (e.g. Chute, Magnesium, Peripheral Vision, Soft Pallet)
    \item \res{https://www.youtube.com/watch?v=hlIRjfto7o0}{Life Lessons Learned Through Contact Improvisation} - An interesting TEDx video-clip to see the connection of CI to general life
    \item \res{https://www.youtube.com/watch?v=n1D9RU2GbBo}{Gogolfest 2016 Contact Improvisation} - Simply aspiring to watch
    \item \res{https://www.youtube.com/watch?v=H8JiB2Nv5Qo}{A couple of basic exercises} - Something to practice by yourself as a beginner
    \item \res{https://www.youtube.com/watch?v=_82Od5NM4LI}{Steve Paxton Talking Dance} - The creator of CI giving an almost 2 hours talk about dancing, 2015
\end{itemize}

